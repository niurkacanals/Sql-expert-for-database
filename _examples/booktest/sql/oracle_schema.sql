CREATE TABLE authors (
  author_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY CONSTRAINT authors_pkey PRIMARY KEY,
  name NVARCHAR2(255) NOT NULL
);

CREATE INDEX authors_name_idx ON authors(name);

CREATE TABLE books (
  book_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY CONSTRAINT books_pkey PRIMARY KEY,
  author_id NUMBER NOT NULL CONSTRAINT books_author_id_fkey REFERENCES authors(author_id),
  isbn NVARCHAR2(255) NOT NULL CONSTRAINT books_isbn_key UNIQUE,
  title NVARCHAR2(255) NOT NULL,
  year NUMBER(4) DEFAULT 2000 NOT NULL,
  available TIMESTAMP WITH TIME ZONE NOT NULL,
  tags NVARCHAR2(255) DEFAULT '' NOT NULL
);

CREATE INDEX books_title_idx ON books(title, year);
